from ultralytics import YOLO


def main():
    """
    室内无人机俯拍地面特定靶子检测

    场景特点：
      - 只有 7 个特定靶子，不是识别"这一类物体"
      - 靶子颜色、纹理、形状完全固定
      - 室内光照恒定、背景统一、无遮挡
      - 高度固定、垂直向下 → 尺度/透视不变
      - 唯一变量：靶子摆放角度
    """
    model = YOLO("yolov8n.pt")

    results = model.train(
        # ============================================================
        # 基础配置
        # ============================================================
        data="my_dataset/data.yaml",
        epochs=200,
        imgsz=640,
        patience=30,
        project=".",
        name="opt_drone",

        # ============================================================
        # 数据增强
        # ============================================================

        # -------- 核心：旋转（靶子朝向随机）--------
        # 靶子放在地面角度完全随机，360° 旋转不变性是核心能力
        degrees=90,
        # 左右翻转：保留，配合旋转覆盖所有朝向
        fliplr=0.5,

        # -------- 位置变化 --------
        # 靶子随机摆在地面各个位置，不能让模型有"目标永远在中间"的偏见
        translate=0.3,
        # 靶子1m×1m，原始拍摄1.2m高，补拍1.8m高 → 目标大小差33%
        # scale=0.5 (0.5x~1.5x) 覆盖从0.8m到2.4m的高度变化，把两种高度的数据统一
        scale=0.2,

        # -------- 模拟镜头边缘畸变 --------
        # 广角镜头画面边缘有桶形畸变，靶子靠近边框会被拉伸变形
        # perspective 做四点透视扭曲，近似边缘拉伸效果
        perspective=0.0005,
        # shear 模拟边缘切向拉伸
        shear=3,

        # -------- 颜色（室内光照恒定，靶子颜色是识别依据，不动）--------
        hsv_h=0.0,
        hsv_s=0.0,
        hsv_v=0.0,

        # -------- 遮挡模拟 --------
        # 室内无遮挡，关闭随机擦除
        erasing=0.0,

        # -------- 混合增强 --------
        # 训练背景多样、比赛背景统一 → 模型容易把背景当识别线索
        # mosaic 把靶子放到不同背景上，逼模型只看靶子不看地面
        mosaic=1.0,
        # 训练集每图单目标，比赛可能有多个靶子同框
        copy_paste=0.15,

        # ============================================================
        # 正则化
        # ============================================================
        dropout=0.1,
        cos_lr=True,

        # ============================================================
        # 优化器
        # ============================================================
        lr0=0.01,
        lrf=0.01,
        momentum=0.937,
        weight_decay=0.0005,
        warmup_epochs=3.0,
        workers=4,                     # 减少并行进程，防止 mixup 内存溢出，不影响精度
    )

    metrics = model.val()
    print(f"\n=== 训练完成 ===")
    print(f"mAP50:    {metrics.box.map50:.4f}")
    print(f"mAP50-95: {metrics.box.map75:.4f}")


if __name__ == "__main__":
    main()
